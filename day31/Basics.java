class Basics
{
  public static void main(String args[])
  {
    System.out.println("First day of Java Coding"); // String Displaying
    System.out.println(1); // Number Displaying
    /* Multiline Comment:  Comments can be used to explain Java code, and to make it more readable. */
    String name = "Harikrishna";
    System.out.println(name);
    int myNum = 8;
    System.out.println(myNum);
    float myFloatNum = 5.99f;
    System.out.println(myFloatNum);
    char myLetter = 'H';
    System.out.println(myLetter);
    boolean myBool = true;
    System.out.println(myBool);
  }
}